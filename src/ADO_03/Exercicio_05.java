    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        Random rng = new Random();
        int machine = rng.nextInt(3);

        if (rdPedra.isSelected() && machine == 0) {
            JOptionPane.showMessageDialog(panel, "Empate");

        } else if (rdPedra.isSelected() && machine == 1) {
            perdeu++;

        } else if (rdPedra.isSelected() && machine == 2) {
            venceu++;
        } else if (rdPapel.isSelected() && machine == 0) {
            venceu++;
        } else if (rdPapel.isSelected() && machine == 1) {
            JOptionPane.showMessageDialog(panel, "Empate");

        } else if (rdPapel.isSelected() && machine == 2) {
            perdeu++;

        } else if (rdTesoura.isSelected() && machine == 0) {
            perdeu++;

        } else if (rdTesoura.isSelected() && machine == 1) {
            venceu++;

        } else if (rdTesoura.isSelected() && machine == 2) {
            JOptionPane.showMessageDialog(panel, "Empate");

        }

        lblWin.setText(Integer.toString(venceu));
        lblLoser.setText(Integer.toString(perdeu));
    }//GEN-LAST:event_jButton1ActionPerformed

